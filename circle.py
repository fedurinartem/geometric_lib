import math


def area(r):
    ''' Принимает число,которое явялется радиусом окружности, и возвращает число, которое является площадью этой окружности'''
    return math.pi * r * r


def perimeter(r):
    ''' Принимает число,которое явялется радиусом окружности, и возвращает число, которое является периметром этой окружности'''
    return 2 * math.pi * r

